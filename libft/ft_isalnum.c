#include "libft.h"

int	ft_isalnum(int c)
{
	if (c < 0 || c > 255)
		return (0);
	if (ft_isalpha(c) != 0)
		return (1);
	if (ft_isdigit(c) != 0)
		return (1);
	return (0);
}
