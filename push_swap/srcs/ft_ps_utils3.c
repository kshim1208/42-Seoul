#include "../include/push_swap.h"

int	ft_isspace(int c)
{
	if (c == ' ' || c == '\t' || c == '\n'
		|| c == '\v' || c == '\f' || c == '\r')
		return (1);
	return (0);
}

int	ft_is_sign(int c)
{
	if (c == '+' || c == '-')
		return (1);
	return (0);
}
